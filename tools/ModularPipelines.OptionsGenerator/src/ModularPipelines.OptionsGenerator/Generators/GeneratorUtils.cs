        sb.AppendLine("#nullable enable");
        sb.AppendLine();
    }

    /// <summary>
    /// Converts a CLI option name to a valid C# enum name.
    /// Example: "--verbosity" -> "Verbosity", "--dry-run" -> "DryRun"
    /// </summary>
    public static string ToEnumName(string optionName, string commandPrefix)
    {
        var cleaned = optionName.TrimStart('-');
        var pascalCase = ToPascalCase(cleaned);
        return $"{commandPrefix}{pascalCase}";
    }

    /// <summary>
    /// Converts a CLI enum value to a valid C# enum member name.
    /// Example: "quiet" -> "Quiet", "dry-run" -> "DryRun", "1" -> "Value1"
    /// </summary>
    public static string ToEnumMemberName(string cliValue)
    {
        if (string.IsNullOrWhiteSpace(cliValue))
        {
            return "Unknown";
        }

        // Handle numeric values
        if (char.IsDigit(cliValue[0]))
        {
            return $"Value{cliValue}";
        }

        // Convert to PascalCase
        var pascalCase = ToPascalCase(cliValue);

        // Ensure it starts with a letter
        if (!char.IsLetter(pascalCase[0]))
        {
            pascalCase = $"Value{pascalCase}";
        }

        return pascalCase;
    }

    /// <summary>
    /// Converts a string to PascalCase.
    /// Handles kebab-case, snake_case, and space-separated words.
    /// Ensures the result is a valid C# identifier (starts with a letter).
    /// </summary>
    public static string ToPascalCase(string input)
    {
        if (string.IsNullOrWhiteSpace(input))
        {
            return string.Empty;
        }

        // Split on common separators
        var words = SeparatorPattern().Split(input);
        var sb = new StringBuilder();

        foreach (var word in words)
        {
            if (string.IsNullOrWhiteSpace(word))
            {
                continue;
            }

            // Capitalize first letter, lowercase the rest
            sb.Append(char.ToUpperInvariant(word[0]));
            if (word.Length > 1)
            {
                sb.Append(word[1..].ToLowerInvariant());
            }
        }

        var result = sb.ToString();

        // Ensure the result is a valid C# identifier (must start with a letter or underscore)
        // Handles cases like "9p" (filesystem protocol) -> "_9p"
        if (result.Length > 0 && !char.IsLetter(result[0]))
        {
            result = "_" + result;
        }

        return result;
    }

    [GeneratedRegex(@"[-_\s]+")]
    private static partial Regex SeparatorPattern();

    [GeneratedRegex(@"(?:/home/runner|/Users/runner(?:admin)?|[A-Za-z]:\\Users\\runneradmin)(?=[/\\])")]
    private static partial Regex RunnerHomePathPattern();

    /// <summary>
    /// C# reserved keywords that cannot be used as identifiers without escaping.
    /// </summary>
    private static readonly HashSet<string> CSharpKeywords = new(StringComparer.Ordinal)
    {
        "abstract", "as", "base", "bool", "break", "byte", "case", "catch", "char", "checked",
        "class", "const", "continue", "decimal", "default", "delegate", "do", "double", "else",
        "enum", "event", "explicit", "extern", "false", "finally", "fixed", "float", "for",
        "foreach", "goto", "if", "implicit", "in", "int", "interface", "internal", "is", "lock",
        "long", "namespace", "new", "null", "object", "operator", "out", "override", "params",
        "private", "protected", "public", "readonly", "ref", "return", "sbyte", "sealed",
        "short", "sizeof", "stackalloc", "static", "string", "struct", "switch", "this",
        "throw", "true", "try", "typeof", "uint", "ulong", "unchecked", "unsafe", "ushort",
        "using", "virtual", "void", "volatile", "while"
    };

    /// <summary>
    /// Escapes a C# identifier if it's a reserved keyword by prefixing with @.
    /// </summary>
    public static string EscapeIdentifier(string? identifier)
    {
        if (string.IsNullOrEmpty(identifier))
        {
            return string.Empty;
        }

        return CSharpKeywords.Contains(identifier) ? $"@{identifier}" : identifier;
    }

    /// <summary>
    /// Generates the CLI attribute string for an option definition.
    /// Used by both OptionsClassGenerator and GlobalOptionsBaseGenerator.
    /// </summary>
    /// <param name="option">The CLI option definition.</param>
    /// <returns>The attribute string (e.g., "CliFlag(\"--verbose\")" or "CliOption(\"--output\")").</returns>
    public static string GenerateCliAttributeString(CliOptionDefinition option)
    {
        if (option.IsFlag)
        {
            // Use CliFlag for boolean flags
            var parts = new List<string> { $"\"{option.SwitchName}\"" };

            if (!string.IsNullOrEmpty(option.ShortForm))
            {
                parts.Add($"ShortForm = \"{option.ShortForm}\"");
            }

            return $"CliFlag({string.Join(", ", parts)})";
        }

        // Use CliOption for value options
        var optionParts = new List<string> { $"\"{option.SwitchName}\"" };

        if (!string.IsNullOrEmpty(option.ShortForm))
        {
            optionParts.Add($"ShortForm = \"{option.ShortForm}\"");
        }

        if (option.ValueSeparator == "=")
        {
            optionParts.Add("Format = OptionFormat.EqualsSeparated");
        }
        else if (option.ValueSeparator == ":")
        {
            optionParts.Add("Format = OptionFormat.ColonSeparated");
        }
        else if (option.ValueSeparator != " " && !string.IsNullOrEmpty(option.ValueSeparator))
        {
            optionParts.Add($"CustomSeparator = \"{option.ValueSeparator}\"");
        }

        if (option.AcceptsMultipleValues)
        {
            optionParts.Add("AllowMultiple = true");
        }

        return $"CliOption({string.Join(", ", optionParts)})";
    }

    /// <summary>
    /// Generates validation attribute lines for an option with validation constraints.
    /// </summary>
    /// <param name="sb">The StringBuilder to append to.</param>
    /// <param name="constraints">The validation constraints.</param>
    /// <param name="indent">The indentation string (defaults to 4 spaces).</param>
    public static void GenerateValidationAttributes(StringBuilder sb, CliValidationConstraints constraints, string indent = "    ")
    {
        ArgumentNullException.ThrowIfNull(sb);
        ArgumentNullException.ThrowIfNull(constraints);

        if (constraints.MinValue.HasValue || constraints.MaxValue.HasValue)
        {
            var min = constraints.MinValue ?? int.MinValue;
            var max = constraints.MaxValue ?? int.MaxValue;
            sb.AppendLine($"{indent}[Range({min}, {max})]");
        }

        if (!string.IsNullOrEmpty(constraints.Pattern))
        {
            sb.AppendLine($"{indent}[RegularExpression(@\"{constraints.Pattern}\")]");
        }
    }

    /// <summary>
    /// Generates XML documentation block for a description.
    /// </summary>
    /// <param name="sb">The StringBuilder to append to.</param>
    /// <param name="description">The description text.</param>
    /// <param name="indent">The indentation string (defaults to 4 spaces).</param>
    public static void GenerateXmlDocumentation(StringBuilder sb, string? description, string indent = "    ")
    {
        ArgumentNullException.ThrowIfNull(sb);

        if (!string.IsNullOrEmpty(description))
        {
            sb.AppendLine($"{indent}/// <summary>");
            sb.AppendLine($"{indent}/// {EscapeXmlComment(description)}");
            sb.AppendLine($"{indent}/// </summary>");
        }
    }

    /// <summary>
    /// Generates a C# method name from CLI command parts.
    /// Converts command parts to PascalCase method name.
    /// E.g., ["container", "create"] -> "ContainerCreate", ["build-server"] -> "BuildServer"
    /// Returns "Execute" for empty command parts (single-command tools like ansible).
    /// </summary>
    /// <param name="commandParts">The command parts array.</param>
    /// <returns>The method name in PascalCase, or "Execute" if command parts is empty.</returns>
    public static string GenerateMethodNameFromCommandParts(string[] commandParts)
    {
        if (commandParts.Length == 0)
        {
            return "Execute";
        }

        return string.Join("", commandParts.Select(ToPascalCase));
    }

    /// <summary>
    /// Generates a C# method name from the last part of a CLI command.
    /// Used for sub-domain commands where the method name is derived from the last segment.
    /// E.g., ["network", "create"] uses "create" -> "Create"
    /// </summary>
    /// <param name="command">The CLI command definition.</param>
    /// <returns>The method name in PascalCase, or "Execute" if no command parts.</returns>
    public static string GenerateMethodNameFromLastCommandPart(CliCommandDefinition command)
    {
        if (command.CommandParts.Length == 0)
        {
            return "Execute";
        }

        return ToPascalCase(command.CommandParts[^1]);
    }

    /// <summary>
    /// The executionOptions parameter as emitted in every generated command method.
    /// </summary>
    public const string ExecutionOptionsParameter = "CommandExecutionOptions? executionOptions = null";

    /// <summary>
    /// Whether a command has required members that must be constructor/method parameters.
    /// </summary>
    public static bool HasRequiredParameters(CliCommandDefinition command)
    {
        ArgumentNullException.ThrowIfNull(command);

        return command.RequiredOptions.Count > 0 ||
               command.PositionalArguments.Any(p => p.IsRequired);
    }

    /// <summary>
    /// Builds the options parameter for a generated command method. Shared by the
    /// interface and implementation generators so their signatures can never diverge.
    /// Optional options must be nullable: generated files declare #nullable enable, so a
    /// null default on a non-nullable reference type produces CS8625.
    /// </summary>
    public static string BuildOptionsParameter(CliCommandDefinition command)
    {
        return HasRequiredParameters(command)
            ? $"{command.ClassName} options"
            : $"{command.ClassName}? options = null";
    }

    /// <summary>
    /// Generates a service method that executes a CLI command.
    /// Used by ServiceImplementationGenerator and SubDomainClassGenerator to avoid duplication.
    /// </summary>
    /// <param name="sb">The StringBuilder to append to.</param>
    /// <param name="methodName">The name of the method to generate.</param>
    /// <param name="command">The CLI command definition.</param>
    /// <param name="includeXmlDoc">Whether to include XML documentation.</param>
    /// <param name="indent">The indentation string (defaults to 4 spaces).</param>
    public static void GenerateServiceMethod(
        StringBuilder sb,
        string methodName,
        CliCommandDefinition command,
        bool includeXmlDoc = true,
        string indent = "    ")
    {
        ArgumentNullException.ThrowIfNull(sb);
        ArgumentNullException.ThrowIfNull(command);

        var hasRequiredParams = HasRequiredParameters(command);

        // XML documentation
        if (includeXmlDoc && !string.IsNullOrEmpty(command.Description))
        {
            GenerateXmlDocumentation(sb, command.Description, indent);
            sb.AppendLine($"{indent}/// <param name=\"options\">The command options.</param>");
            sb.AppendLine($"{indent}/// <param name=\"executionOptions\">The execution configuration options.</param>");
            sb.AppendLine($"{indent}/// <param name=\"cancellationToken\">Cancellation token.</param>");
            sb.AppendLine($"{indent}/// <returns>The command result.</returns>");
        }
        else
        {
            sb.AppendLine($"{indent}/// <inheritdoc />");
        }

        sb.AppendLine($"{indent}public virtual async Task<CommandResult> {methodName}(");
        sb.AppendLine($"{indent}    {BuildOptionsParameter(command)},");
        sb.AppendLine($"{indent}    {ExecutionOptionsParameter},");
        sb.AppendLine($"{indent}    CancellationToken cancellationToken = default)");
        sb.AppendLine($"{indent}{{");

        if (hasRequiredParams)
        {
            sb.AppendLine($"{indent}    return await _command.ExecuteCommandLineTool(options, executionOptions, cancellationToken);");
        }
        else
        {
            sb.AppendLine($"{indent}    return await _command.ExecuteCommandLineTool(options ?? new {command.ClassName}(), executionOptions, cancellationToken);");
        }

        sb.AppendLine($"{indent}}}");
    }

    /// <summary>
    /// Secret-related keywords that indicate an option should be obfuscated in logs.
    /// </summary>
    private static readonly string[] SecretKeywords =
    [
        "Secret",
        "Password",
        "Passphrase",
        "Token",
        "Credential",
        "ApiKey",
        "PrivateKey",
        "AccessKey",
        "SecretKey"
    ];

    /// <summary>
    /// Determines if an option should be marked as a secret based on its property name.
    /// Options containing keywords like "Secret", "Password", "Token", "Key", or "Credential"
    /// in their name are considered secrets and should be obfuscated in logs.
    /// </summary>
    /// <param name="propertyName">The C# property name of the option.</param>
    /// <param name="isFlag">Whether this is a boolean flag (flags are not considered secrets).</param>
    /// <returns>True if the option should be marked as a secret.</returns>
    public static bool IsSecretOption(string propertyName, bool isFlag)
    {
        // Flags (boolean options) don't take values, so they can't be secrets
        if (isFlag)
        {
            return false;
        }

        if (string.IsNullOrEmpty(propertyName))
        {
            return false;
        }

        // Check if the property name contains any secret-related keywords
        return SecretKeywords.Any(keyword =>
            propertyName.Contains(keyword, StringComparison.OrdinalIgnoreCase));
    }

    /// <summary>
    /// Throws if two generated files resolve to the same output path (case-insensitive).
    /// Without this check, generators that compute colliding paths silently overwrite each
    /// other (last writer wins), which is how single-command tools ended up shipping an
    /// abstract options class where a concrete command options class was expected. Paths
    /// differing only by case are also rejected because they collide on case-insensitive
    /// filesystems.
    /// </summary>
    /// <param name="files">The files produced for the current tool.</param>
    /// <param name="previouslyEmittedPaths">
    /// Normalized (forward-slash) paths already emitted earlier in the run, so tools
    /// sharing an output directory can't overwrite each other's files either.
    /// </param>
    public static void EnsureNoDuplicateFilePaths(
        IEnumerable<GeneratedFile> files,
        IReadOnlySet<string>? previouslyEmittedPaths = null)
    {
        ArgumentNullException.ThrowIfNull(files);

        var normalizedPaths = files
            .Select(f => f.RelativePath.Replace('\\', '/'))
            .ToList();

        var duplicates = normalizedPaths
            .GroupBy(p => p, StringComparer.OrdinalIgnoreCase)
            .Where(g => g.Count() > 1)
            .Select(g => g.Key)
            .Concat(previouslyEmittedPaths is null
                ? []
                : normalizedPaths.Where(previouslyEmittedPaths.Contains))
            .Distinct(StringComparer.OrdinalIgnoreCase)
            .ToList();

        if (duplicates.Count > 0)
        {
            throw new InvalidOperationException(
                $"Multiple generators produced the same output path(s): {string.Join(", ", duplicates)}. " +
                "Each generated file must have a unique, case-insensitively distinct path.");
        }
    }

    /// <summary>
    /// Renames command options classes that share their name with the parent global-options
    /// class. The allocated fallback is guaranteed not to collide with another command class.
    /// </summary>
    public static IReadOnlyList<CliCommandDefinition> NormalizeCommandClassNames(IReadOnlyList<CliCommandDefinition> commands)
    {
        ArgumentNullException.ThrowIfNull(commands);

        if (!commands.Any(command => NeedsClassNameNormalization(command)))
        {
            return commands;
        }

        var occupiedClassNames = commands
            .Select(command => command.ClassName)
            .ToHashSet(StringComparer.OrdinalIgnoreCase);
        var normalizedCommands = new List<CliCommandDefinition>(commands.Count);

        foreach (var command in commands)
        {
            if (!NeedsClassNameNormalization(command))
            {
                normalizedCommands.Add(command);
                continue;
            }

            var className = AllocateExecuteClassName(command, occupiedClassNames);
            normalizedCommands.Add(command with { ClassName = className });
        }

        return normalizedCommands;
    }

    /// <summary>
    /// Returns the root-level commands (no sub-domain) whose generated method name does not
    /// collide with a sub-domain property name. Shared by ServiceInterfaceGenerator and
    /// ServiceImplementationGenerator so the interface and implementation can never disagree
    /// about which methods exist. Colliding single-part commands are not lost: the
    /// SubDomainClassGenerator surfaces them as Execute() on the sub-domain class.
    /// </summary>
    public static IReadOnlyList<CliCommandDefinition> GetNonCollidingRootCommands(CliToolDefinition tool)
    {
        ArgumentNullException.ThrowIfNull(tool);

        var subDomainNames = new HashSet<string>(
            tool.SubDomainGroups.Select(group => GetSubDomainIdentifier(tool, group)),
            StringComparer.OrdinalIgnoreCase);

        var rootCommands = tool.Commands
            .Where(c => c.SubDomainGroup is null)
            .Where(c => !subDomainNames.Contains(GetCommandGroupIdentifier(c)))
            .ToList();

        // Distinct commands can normalize to the same method name (e.g. "build-server"
        // and "build_server" both become BuildServer). Emitting both would produce
        // duplicate members the duplicate-path check cannot see, so fail loudly here.
        var duplicateMethodNames = rootCommands
            .GroupBy(c => GenerateMethodNameFromCommandParts(c.CommandParts), StringComparer.OrdinalIgnoreCase)
            .Where(g => g.Count() > 1)
            .Select(g => $"{g.Key} ({string.Join(", ", g.Select(c => c.FullCommand))})")
            .ToList();

        if (duplicateMethodNames.Count > 0)
        {
            throw new InvalidOperationException(
                $"Multiple {tool.ToolName} commands normalize to the same method name: " +
                $"{string.Join("; ", duplicateMethodNames)}. Rename or exclude one in the scraper.");
        }

        return rootCommands;
    }

    /// <summary>
    /// Gets the generated identifier for a sub-domain while preserving legacy casing unless
    /// the scraper supplied an explicit tool-specific override.
    /// </summary>
    public static string GetSubDomainIdentifier(CliToolDefinition tool, string subDomainGroup)
    {
        ArgumentNullException.ThrowIfNull(tool);
        ArgumentException.ThrowIfNullOrWhiteSpace(subDomainGroup);

        var overrides = tool.Commands
            .Where(command => string.Equals(
                command.SubDomainGroup,
                subDomainGroup,
                StringComparison.OrdinalIgnoreCase))
            .Select(command => command.CommandGroupIdentifierOverride)
            .Where(identifier => identifier is not null)
            .Distinct(StringComparer.OrdinalIgnoreCase)
            .ToList();

        return overrides.Count switch
        {
            0 => ToPascalCase(subDomainGroup),
            1 => overrides[0]!,
            _ => throw new InvalidOperationException(
                $"The {tool.ToolName} scraper produced conflicting identifiers for sub-domain " +
                $"'{subDomainGroup}': {string.Join(", ", overrides)}.")
        };
    }

    private static string GetCommandGroupIdentifier(CliCommandDefinition command) =>
        command.CommandGroupIdentifierOverride
        ?? GenerateMethodNameFromCommandParts(command.CommandParts);

    private static bool NeedsClassNameNormalization(CliCommandDefinition command) =>
        string.Equals(command.ClassName, command.ParentClassName, StringComparison.OrdinalIgnoreCase);

    private static string AllocateExecuteClassName(
        CliCommandDefinition command,
        ISet<string> occupiedClassNames)
    {
        var baseName = command.ParentClassName.EndsWith("Options", StringComparison.Ordinal)
            ? command.ParentClassName[..^"Options".Length]
            : command.ParentClassName;
        var suffix = "Execute";
        var candidate = $"{baseName}{suffix}Options";

        while (!occupiedClassNames.Add(candidate))
        {
            suffix += "Execute";
            candidate = $"{baseName}{suffix}Options";
        }

        return candidate;
    }

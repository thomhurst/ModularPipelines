    /// <summary>
    /// Escapes text for use in XML documentation comments.
    /// </summary>
    public static string EscapeXmlComment(string? text)
    {
        if (string.IsNullOrEmpty(text))
        {
            return string.Empty;
        }

        // Help text scraped on a CI runner can embed the runner's home directory in
        // option defaults (e.g. "/home/runner/.config/helm/..."). Those paths are
        // meaningless to consumers, so normalize them to "~" before shipping docs.
        text = RunnerHomePathPattern().Replace(text, "~");

        return text
            .Replace("&", "&amp;")
            .Replace("<", "&lt;")
            .Replace(">", "&gt;")
            .Replace("\r\n", " ")
            .Replace("\n", " ")
            .Replace("\r", " ")
            .Trim();
    }

    /// <summary>
    /// Generates a standard auto-generated file header.
    /// Note: Generation date is stored in AssemblyInfo.Generated.cs, not in each file,
    /// to avoid unnecessary diffs when regenerating unchanged files.
    /// </summary>
    /// <param name="sb">The StringBuilder to append to.</param>
    /// <param name="sourceUrl">Optional documentation source URL.</param>
    public static void GenerateFileHeader(StringBuilder sb, string? sourceUrl = null)
    {
        ArgumentNullException.ThrowIfNull(sb);

        sb.AppendLine("// <auto-generated>");
        sb.AppendLine("// This file was generated by ModularPipelines.OptionsGenerator.");
        if (!string.IsNullOrEmpty(sourceUrl))
        {
            sb.AppendLine($"// Source: {sourceUrl}");
        }
        sb.AppendLine("// Do not edit this file manually.");
        sb.AppendLine("// </auto-generated>");
        sb.AppendLine();
    }

    /// <summary>
    /// Generates an AssemblyInfo file with generation metadata.
    /// Deliberately contains no timestamp: embedding the generation time made every
    /// weekly run produce a diff (and therefore a PR) even when nothing else changed.
    /// </summary>
    /// <param name="targetNamespace">The target namespace for the assembly.</param>
    /// <param name="toolName">The CLI tool name.</param>
    /// <returns>The generated AssemblyInfo content.</returns>
    public static string GenerateAssemblyInfo(string targetNamespace, string toolName)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated>");
        sb.AppendLine("// This file was generated by ModularPipelines.OptionsGenerator.");
        sb.AppendLine("// Do not edit this file manually.");
        sb.AppendLine("// </auto-generated>");
        sb.AppendLine();
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using System.Reflection;");
        sb.AppendLine();
        sb.AppendLine($"[assembly: AssemblyMetadata(\"ModularPipelines.OptionsGenerator.Tool\", \"{toolName}\")]");
        sb.AppendLine($"[assembly: AssemblyMetadata(\"ModularPipelines.OptionsGenerator.Version\", \"{GeneratorVersion}\")]");
        return sb.ToString();
    }


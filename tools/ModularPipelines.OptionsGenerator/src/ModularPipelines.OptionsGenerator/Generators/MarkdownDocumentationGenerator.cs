using System.Text;
using System.Text.RegularExpressions;
using ModularPipelines.OptionsGenerator.Models;

namespace ModularPipelines.OptionsGenerator.Generators;

/// <summary>
/// Generates a Docusaurus command-reference page from the same metadata as the CLI API.
/// </summary>
public partial class MarkdownDocumentationGenerator : ICodeGenerator, IGeneratedFileCleanupProvider
{
    private const string GeneratedMarker = "<!-- This file is generated by ModularPipelines.OptionsGenerator. -->";
    private static readonly string DocumentationDirectory = Path.Combine("docs", "docs", "mp-packages", "cli");

    public Task<IReadOnlyList<GeneratedFile>> GenerateAsync(
        CliToolDefinition tool,
        CancellationToken cancellationToken = default)
    {
        var file = new GeneratedFile
        {
            RelativePath = Path.Combine(DocumentationDirectory, GetFileName(tool.ToolName)),
            Content = GenerateMarkdown(tool),
        };

        return Task.FromResult<IReadOnlyList<GeneratedFile>>([file]);
    }

    public IEnumerable<GeneratedFileCleanupRule> GetCleanupRules(IReadOnlyCollection<string> toolNames)
    {
        yield return new GeneratedFileCleanupRule
        {
            RelativeDirectory = DocumentationDirectory,
            SearchPattern = "*.md",
            GeneratedMarker = GeneratedMarker,
            ExpectedFileNames = toolNames.Select(GetFileName).ToHashSet(StringComparer.OrdinalIgnoreCase),
        };
    }

    private static string GenerateMarkdown(CliToolDefinition tool)
    {
        var commands = GetExposedCommands(tool);
        var sb = new StringBuilder();

        sb.AppendLine("---");
        sb.AppendLine($"title: {tool.ToolName} CLI reference");
        sb.AppendLine("---");
        sb.AppendLine(GeneratedMarker);
        sb.AppendLine();
        sb.AppendLine($"# {tool.ToolName} CLI reference");
        sb.AppendLine();
        sb.AppendLine($"`{tool.TargetNamespace}` provides strongly typed access to the `{tool.ToolName}` CLI.");
        sb.AppendLine();
        sb.AppendLine("## Installation");
        sb.AppendLine();
        sb.AppendLine("```shell");
        sb.AppendLine($"dotnet add package {tool.TargetNamespace}");
        sb.AppendLine("```");
        sb.AppendLine();
        sb.AppendLine($"Import `{tool.TargetNamespace}.Extensions`, then resolve the service with `context.{tool.NamespacePrefix}()`.");
        sb.AppendLine();
        AppendExample(sb, tool);
        sb.AppendLine("## Commands");
        sb.AppendLine();
        sb.AppendLine("| CLI command | Options record |");
        sb.AppendLine("| --- | --- |");

        foreach (var command in commands.OrderBy(command => command.FullCommand, StringComparer.OrdinalIgnoreCase))
        {
            sb.AppendLine($"| `{EscapeTableCell(command.FullCommand)}` | `{command.ClassName}` |");
        }

        return sb.ToString();
    }

    private static void AppendExample(StringBuilder sb, CliToolDefinition tool)
    {
        sb.AppendLine("## Module example");
        sb.AppendLine();

        var rootCommands = GeneratorUtils.GetNonCollidingRootCommands(tool);
        var command = rootCommands.Count > 0 ? rootCommands[0] : null;
        if (command is null)
        {
            sb.AppendLine("Resolve the service in a module, then select a generated sub-domain and command from the table below:");
            sb.AppendLine();
            sb.AppendLine("```csharp");
            sb.AppendLine($"using {tool.TargetNamespace}.Extensions;");
            sb.AppendLine();
            sb.AppendLine($"var {ToCamelCase(tool.NamespacePrefix)} = context.{tool.NamespacePrefix}();");
            sb.AppendLine("```");
            sb.AppendLine();
            return;
        }

        var methodName = GeneratorUtils.GenerateMethodNameFromCommandParts(command.CommandParts);
        var constructorArguments = GeneratorUtils.GetRequiredConstructorParameters(command)
            .Select(parameter => ExampleValue(parameter.CSharpType));

        sb.AppendLine("```csharp");
        sb.AppendLine("using ModularPipelines.Context;");
        sb.AppendLine("using ModularPipelines.Models;");
        sb.AppendLine("using ModularPipelines.Modules;");
        sb.AppendLine($"using {tool.TargetNamespace}.Extensions;");
        sb.AppendLine($"using {tool.TargetNamespace}.Options;");
        sb.AppendLine();
        sb.AppendLine("public class RunCommandModule : Module<CommandResult>");
        sb.AppendLine("{");
        sb.AppendLine("    protected override async Task<CommandResult?> ExecuteAsync(");
        sb.AppendLine("        IModuleContext context,");
        sb.AppendLine("        CancellationToken cancellationToken)");
        sb.AppendLine("    {");
        sb.AppendLine($"        return await context.{tool.NamespacePrefix}().{methodName}(");
        sb.AppendLine($"            new {command.ClassName}({string.Join(", ", constructorArguments)}),");
        sb.AppendLine("            cancellationToken: cancellationToken);");
        sb.AppendLine("    }");
        sb.AppendLine("}");
        sb.AppendLine("```");
        sb.AppendLine();
    }

    private static IReadOnlyList<CliCommandDefinition> GetExposedCommands(CliToolDefinition tool)
    {
        var rootCommands = GeneratorUtils.GetNonCollidingRootCommands(tool)
            .Select(command => command.ClassName)
            .ToHashSet(StringComparer.Ordinal);

        return tool.Commands
            .Where(command => command.SubDomainGroup is not null || rootCommands.Contains(command.ClassName))
            .DistinctBy(command => command.ClassName)
            .ToList();
    }

    private static string ExampleValue(string cSharpType)
    {
        var type = cSharpType.TrimEnd('?');
        if (type == "string")
        {
            return "\"value\"";
        }

        if (type.Contains("string", StringComparison.Ordinal)
            && (type.EndsWith("[]", StringComparison.Ordinal)
                || type.StartsWith("IEnumerable<", StringComparison.Ordinal)
                || type.StartsWith("IReadOnly", StringComparison.Ordinal)
                || type.StartsWith("List<", StringComparison.Ordinal)))
        {
            return "[\"value\"]";
        }

        return type switch
        {
            "bool" => "true",
            "byte" or "short" or "int" or "long" or "float" or "double" or "decimal" => "1",
            "TimeSpan" => "TimeSpan.FromSeconds(30)",
            _ => "default!",
        };
    }

    private static string EscapeTableCell(string value) => value.Replace("|", "\\|", StringComparison.Ordinal);

    private static string ToCamelCase(string value) => char.ToLowerInvariant(value[0]) + value[1..];

    private static string GetFileName(string toolName) => $"{Slugify(toolName)}.md";

    private static string Slugify(string value) =>
        RepeatedHyphenRegex().Replace(NonSlugCharacterRegex().Replace(value.ToLowerInvariant(), "-"), "-").Trim('-');

    [GeneratedRegex("[^a-z0-9-]+")]
    private static partial Regex NonSlugCharacterRegex();

    [GeneratedRegex("-+")]
    private static partial Regex RepeatedHyphenRegex();
}
